    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_rubik", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into cubos values(?,?,?,?)");
            
            pst.setString(1, "0");
            pst.setString(2, txt_caras.getText().trim());
            pst.setString(3, txt_dimension.getText().trim());
            pst.setString(4, txt_precio.getText().trim());
            pst.executeUpdate();
            
            txt_caras.setText("");
            txt_dimension.setText("");
            txt_precio.setText("");
            label_status.setText("Registrado con exito");
        }catch (Exception e){
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_rubik", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from cubos where ID = ?");
            pst.setString(1, txt_buscar.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                txt_caras.setText(rs.getString("NumeroCaras"));
                txt_dimension.setText(rs.getString("Dimension"));
                txt_precio.setText(rs.getString("Precio"));
            }else{
                JOptionPane.showMessageDialog(null, "Cubo no registrado");
            }
            
        }catch (Exception e){
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed


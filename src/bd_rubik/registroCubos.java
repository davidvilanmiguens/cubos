    private void mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarActionPerformed
        try {

            DefaultTableModel tabla = (DefaultTableModel) Tabla.getModel();
            Connection sol = DriverManager.getConnection("jdbc:mysql://localhost/bd_rubik","root","");
            Statement stmt = sol.createStatement();
            ResultSet rs = stmt.executeQuery("select * from cubos");
            tabla.setRowCount(0);

            if(rs.next()){

                do{
                    String[] fila= {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)};
                    tabla.addRow(fila);

                }while(rs.next());
            }
        } catch (Exception a) {

        }
    }//GEN-LAST:event_mostrarActionPerformed

